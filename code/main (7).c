void readRPM()

{
  if(REV >= 10 || HAL_GetTick()>=st+1000)                  //  IT WILL UPDATE AFTER EVERY 10 READINGS or 1 second in idle

  {
     rpm = (60/2)*(1000/(HAL_GetTick() - time))*REV/slots;
     time = HAL_GetTick();
     REV = 0;
     RPM=rpm;
     HAL_Delay(500);
     st=HAL_GetTick();
     flag1=1;
     HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_14);

   }

}

