/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	//radius=((radius * 2.54)/100.0);  // converting inches to  meter

			//circ=2.0 * 3.14 * radius;		// calculates the circumference of the circle, which is the distance traveled in one revolution.

			circ=1.9;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_CAN1_Init();
  MX_ADC1_Init();
  MX_ADC2_Init();
  /* USER CODE BEGIN 2 */
  HAL_CAN_Start(&hcan1);
  HAL_CAN_ActivateNotification(&hcan1, CAN_IT_RX_FIFO0_MSG_PENDING);

  TxHeader.DLC = 8;
  TxHeader.RTR = CAN_RTR_DATA;
  TxHeader.IDE = CAN_ID_STD;
  TxHeader.StdId = 0x1AA; // Standard Identifier
  TxHeader.TransmitGlobalTime = DISABLE;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    double Vout, Rth, temperature;

    readRPM(); //calculates rpm
   	distance=(pulse/slots);
   	//calculates the distance traveled per pulse
   	  speed= ((float)RPM * circ)/60;
   	   // RPM (Revolutions Per Minute) is converted to RPS (Revolutions Per Second) by dividing by 60.
   	  //To find the speed, multiply the number of revolutions per second by the distance traveled per revolution (which is the circumference).
   	  TxData[2] = (uint8_t)speed;

   	  if(flag1)
   	  {
   	  if (HAL_CAN_AddTxMessage(&hcan1, &TxHeader, TxData, &TxMailBox) != HAL_OK){

   	  	  Error_Handler();

   	     HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_13);

   	  }

   	  flag1=0;

   	  }

   	  HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_15);

    if (HAL_ADC_Start(&hadc1) != HAL_OK)
    {
      Error_Handler();
    }

    // Wait for conversion to complete
    if (HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY) == HAL_OK)
    {
      // Read ADC value
      float adc_value = HAL_ADC_GetValue(&hadc1);

      // Convert ADC value to voltage
      v_out = (adc_value * 12.0) / 4095.0;

      // Calculate battery percentage
      float battery_percentage = 0.0;
      if (v_out >= minBatteryVoltage)
      {
        battery_percentage = (v_out - minBatteryVoltage) / (maxBatteryVoltage - minBatteryVoltage) * 100.0;
        if (battery_percentage > 100.0)
        {
          battery_percentage = 100.0;
        }
      }

      // Transmit battery voltage and percentage over CAN
//
      TxData[0] = (uint8_t)battery_percentage;



      if (HAL_CAN_AddTxMessage(&hcan1, &TxHeader, TxData, &TxMailBox) != HAL_OK)
      {
        Error_Handler();
      }

      if (HAL_ADC_Start(&hadc2) != HAL_OK)
      {
        Error_Handler();
      }

      // Wait for conversion to complete
      if (HAL_ADC_PollForConversion(&hadc2, HAL_MAX_DELAY) == HAL_OK)
      {
        uint16_t adc_value_t = HAL_ADC_GetValue(&hadc2);

        Vout = (adc_value_t * VCC) / adc_resolution;
        Rth = (VCC * R2 / Vout) - R2;

        temperature = 1 / (A + (B * log(Rth)) + (C * pow(log(Rth), 3)));
        temperature -= 273.15;




        if(temperature >= 45)
        {
        	TxData[1] = 'H'; // High
        }
        else
        {
        	TxData[1] = 'N';
        }

        if (HAL_CAN_AddTxMessage(&hcan1, &TxHeader, TxData, &TxMailBox) != HAL_OK)
        {
          Error_Handler();
        }
      }

      HAL_Delay(1000);
    }
  }
  /* USER CODE END 3 */
}

